# 4. Транслитерация
# Словарь замен для строчных букв (ъ и ь отсутствуют, они будут удалены)
replace = {
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'e',
    'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'i', 'к': 'k', 'л': 'l', 'м': 'm',
    'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
    'ф': 'f', 'х': 'kh', 'ц': 'tc', 'ч': 'ch', 'ш': 'sh', 'щ': 'shch',
    'ы': 'y', 'э': 'e', 'ю': 'iu', 'я': 'ia'
}
# Строка для обработки
text = input()
result = []
for ch in text:
    # Проверяем, русская ли буква (по наличию в словаре в нижнем регистре)
    low_ch = ch.lower()
    if low_ch in replace:
        # Получаем замену для строчной
        repl = replace[low_ch]
        # Если исходная буква была заглавной
        if ch.isupper():
            # Первую букву замены делаем заглавной, остальные строчными
            repl = repl[0].upper() + repl[1:]
        result.append(repl)
    elif low_ch in ('ъ', 'ь'):  # твёрдый и мягкий знаки удаляем
        continue
    else:
        result.append(ch)  # некириллические символы оставляем
print(''.join(result))
